<template>
    <div
        class="filter-input"
        :class="theme"
    >
        <input
            type="text"
            v-bind="$attrs"
            :value="value"
            @input="$emit('input', $event.target.value)"
        >
        <transition
            name="fade"
            :duration="150"
        >
            <button
                v-show="hasValue"
                class="clear-me"
                @click="clear"
            >
                <span>&times;</span>
            </button>
        </transition>
    </div>
</template>

<script>
export default {
    name: 'BaseFilterInput',
    inheritAttrs: false,
    props: {
        value: {
            type: String,
            default: ''
        },

        theme: {
            type: String,
            default: ''
        }
    },

    computed: {
        hasValue () {
            return this.value && this.value.length
        }
    },

    methods: {
        clear () {
            this.$emit('input', null)
        }
    }
}
</script>

