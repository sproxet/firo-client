<template>
    <section class="blockchain">
        <div
            class="status"
            :class="{ 'is-synced': isBlockchainSynced }"
        >
            <base-popover
                trigger="hover"
                :disabled="isBlockchainSynced"
                boundaries-element="body"
                :popover-class="['advice', connectionPopoverClass]"
                placement="bottom-start"
                :offset="4"
