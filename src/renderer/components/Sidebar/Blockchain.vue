                <template slot="target">
                    <template v-if="isBlockchainSynced">
                        <tick-icon class="icon" />
                        <span>{{ $t('navigation.network.label__synced') }}</span>
                    </template>
                    <template v-else>
                        <loading-bounce
                            class="icon"
                            :color="connections ? 'green' : 'comet'"
                            size="mini"
                        />
                        <span v-if="connections">
                            {{ $t('navigation.network.label__syncing') }}
                        </span>
                        <span v-else>
                            {{ $t('navigation.network.label__peers') }}
                        </span>
                    </template>
                </template>
            </base-popover>
        </div>
        <div
            class="connections"
            :class="connectionClass"
        >
            <base-popover
                trigger="hover"
                boundaries-element="body"
                :popover-class="['advice', connectionPopoverClass]"
                placement="bottom"
                :offset="4"
                :delay="{ show: 200, hide: 100 }"
            >
                <blockchain-connection-popover
                    slot="content"
                    :connection-class="connectionClass"
                />
                <template slot="target">
                    <circular-badge
                        class="connections-badge"
                        :color="connectionClass"
                        :content="connections"
                    />
                </template>
            </base-popover>
        </div>

        <div class="sync-progress-wrap">
            <base-popover
                trigger="hover"
                :disabled="isBlockchainSynced"
                boundaries-element="body"
                :popover-class="['advice', blockchainPopoverClass]"
                placement="bottom-start"
                :offset="'12,-8'"
                :delay="{ show: 200, hide: 100 }"
            >
                <blockchain-sync-progress-popover slot="content" />
                <template slot="target">
                    <div
                        class="sync-progress"
                        :class="{ 'is-synced': isBlockchainSynced }"
                    >
                        <div
                            v-show="!isBlockchainSynced"
                            class="wrap"
                        >
                            <div
                                class="loaded"
                                :style="{ width: `${progress}%`}"
                            >
                                <div
                                    v-if="!isBlockchainSynced"
                                    class="loading"
                                >
                                    <div class="bg" />
                                </div>
                            </div>
                        </div>
                    </div>
                </template>
            </base-popover>
        </div>
    </section>
</template>

<script>
import { mapGetters } from 'vuex'

import LoadingBounce from 'renderer/components/Icons/LoadingBounce'
import TickIcon from 'renderer/components/Icons/TickIcon'

import BlockchainConnectionPopover from 'renderer/components/Sidebar/BlockchainConnectionPopover'
import BlockchainSyncProgressPopover from 'renderer/components/Sidebar/BlockchainSyncProgressPopover'
import CircularBadge from 'renderer/components/Badge/CircularBadge'

export default {
    name: 'Blockchain',
    components: {
        CircularBadge,
        LoadingBounce,
        TickIcon,
        BlockchainConnectionPopover,
        BlockchainSyncProgressPopover
    },

    computed: {
        ...mapGetters({
            estimatedBlockHeight: 'Blockchain/estimatedBlockHeight',
            currentBlockHeight: 'Blockchain/currentBlockHeight',
            currentBlockTimestamp: 'Blockchain/currentBlockTimestamp',
            avgBlockTime: 'Blockchain/averageBlockTimeInMilliSeconds',
            isSynced: 'Blockchain/isSynced',
            isBlockchainSynced: 'Blockchain/isBlockchainSynced',
            hasConnections: 'Blockchain/hasConnections',
            connections: 'Blockchain/connections',
            connectedViaTor: 'Settings/isConnectedViaTor'
        }),

        progress () {
            return this.currentBlockHeight / this.estimatedBlockHeight * 100
        },

        connectionPopoverClass () {
            if (!this.hasConnections) return 'error';
            if (!this.connectedViaTor) return 'orange';
            return 'green'
        },

        blockchainPopoverClass () {
            return this.hasConnections ? 'green' : 'error';
        },

        connectionClass () {
            if (!this.hasConnections) return 'error';
            if (!this.connectedViaTor) return 'public';
            return 'private';
        }
    }
}
</script>

